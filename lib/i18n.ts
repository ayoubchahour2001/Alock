export function getCurrentLanguage(): string {
  if (typeof window !== "undefined") {
    return localStorage.getItem("language") || "es"
  }
  return "es"
}

export function setCurrentLanguage(language: string): void {
  if (typeof window !== "undefined") {
    localStorage.setItem("language", language)
  }
}
